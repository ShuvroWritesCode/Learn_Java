public class main {
    public static void main(String[] args){

        //Operators
        //Arithmetic Operators (Binary)

        int a = 5;
        int b = 10;
        double c = 6.5;
        double d = 0.5;
        int sum = a + b;
        int diff = a - b;
        int multi = a * b;
        double div = c / d;
        double mod = c % d;

        //Arithmetic Operators (Unary)

        int num1 = 1;
        System.out.println(num1++);
        System.out.println(num1);
        System.out.println(++num1);
        System.out.println(num1--);
        System.out.println(num1);
        System.out.println(--num1);
    }
}